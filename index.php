                        <ul class="liste">
                            <li>Behandlung zur Rehabilitation.</li>
                            <li>Berufshilfe (z.B. Umschulung eines Unfallverletzten)</li>
                            <li>Geldleistungen (Verletzungsgeld, Pflegegeld, ggf. Übergangsgeld)</li>
                            <li>Unfallrente wird dann gezahlt, wenn die Erwerbsfähigkeit um mindestens 20% gemindert ist. Die Vollrente bei vollständiger Erwerbsunfähigkeit beträgt beispielsweise 2/3 des Jahresarbeitsverdienstes.</li>
                            <li>Die Leistungen sind abhängig von Gehalt bzw. Lohn des Unfallgeschädigten.</li>
                            <li><span class="bold">Hausfrauen und die meisten Selbstständigen sind nicht gesetzlich versichert.</span></li>
                            <li><span class="bold">Viele Folgekosten werden nicht getragen:</span><br>Zum Beispiel die, die für die Hilfe bei der Bewältigung des Alltags anfallen. Oder für die Wiederherstellung der Arbeits- und Leistungsfähigkeit.</li>
                        </ul>
                        <div class="comment">
                            Da die gesetzliche Unfallversicherung nur den Hin- und Rückweg sowie die Arbeitszeit versichert und im Fall der Fälle nur eine Grundsicherung ist, stimmt die Pauschalaussage, dass man bei der gesetzlichen Unfallversicherung 1/3 des Tages versichert ist, nicht!<br>Sowohl für Hausfrauen/-männer als auch für Selbständige fällt selbst die Grundsicherung weg!
