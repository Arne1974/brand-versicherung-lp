                    <div class="subtitle">Die Grundsicherung</div>
                    <div class="text">Der Leistungsumfang der gesetzlichen<br>Unfallversicherung dient dem Anspruch einer<br>Rehabilation und Wiedereingliederung<br>in den Arbeitsprozess.</div>
